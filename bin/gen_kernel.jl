#!/usr/bin/env julia
# Ahead-of-time kernel generator.
#
# Reads the vendored 41.2 KLambda kernel, compiles every form once with the
# shen-julia compiler, and writes:
#
#   src/kernel_generated.jl   top-level `function K_...(...) ... end` method
#                             definitions (so PRECOMPILATION bakes them as real
#                             package methods — no per-boot Core.eval / JIT),
#                             plus `_register_baked_kernel!()` which performs the
#                             setfn! registrations + runs any top-level kernel
#                             expressions at boot.
#   kernel_kdata.jls          the literal table (Compiler.KDATA) the methods index.
#   kernel_arity.jls          the defun arity table (needed at runtime so the
#                             post-boot eval/define codegen emits the same calls).
#
# This is the heart of fast boot: instead of compiling ~1100 KL functions into
# Julia source and Core.eval'ing them on every startup (~12s + JIT), the methods
# live in the module image and boot only restores KDATA/ARITY and runs the cheap
# registrations.
#
# Run: julia --project=. bin/gen_kernel.jl   (then rebuild the sysimage)

using ShenJulia
using ShenJulia: Compiler, Boot, Runtime, Prims
using Serialization

const PROJ = dirname(@__DIR__)
const SRC  = joinpath(PROJ, "src")

isdefun(f) = Runtime.is_cons(f) && Runtime.is_symbol(Compiler.car(f)) && Compiler.car(f).name == "defun"

kldir = Boot.find_kldir()
println("Generating baked kernel from: ", kldir)

# 1. Read + prescan every file first so ARITY is fully populated before any
#    body is compiled (cross-file arity decisions must see all defuns).
allforms = Dict{String,Vector{Any}}()
for nm in Boot.KERNEL_FILES
    forms = Runtime.read_all(read(joinpath(kldir, nm * ".kl"), String))
    allforms[nm] = forms
    Compiler.prescan(forms)
end

# 2. Compile every form in load order. Defuns -> top-level method + registration;
#    any non-defun top-level form -> an expression run at registration time.
# Collect defun methods keyed by name with LAST-WINS semantics (a couple of kernel
# functions — fail, min — are defined in more than one file; the runtime Core.eval
# path lets the last definition overwrite, but two baked top-level `function K_x`
# would be a forbidden duplicate during precompilation). We compile every form (to
# advance gensym/KDATA state deterministically) but keep only the final method/reg
# per name. Top-level non-defun forms (license strings etc.) are kept in order.
order_names = String[]                       # de-duplicated method emission order
method_of   = Dict{String,String}()          # name -> method source
reg_of      = Dict{String,String}()          # name -> setfn! registration
toplevel    = String[]
nskipped = 0
for nm in Boot.KERNEL_FILES
    for f in allforms[nm]
        if isdefun(f)
            name = Compiler.car(Compiler.cdr(f)).name
            # Kernel defuns that share a name with a host primitive (thaw, not, empty?,
            # boolean?, integer?, symbol?, string->symbol): the prim already defines
            # K_<name> at module load, and its host implementation is the one shen-julia
            # intends to use. Compile (for determinism) but drop the kernel copy.
            if name in Compiler.PRIMS
                Compiler.cdefun_parts(f)
                global nskipped += 1
                continue
            end
            _, arity, ident, msrc = Compiler.cdefun_parts(f)
            haskey(method_of, name) || push!(order_names, name)
            method_of[name] = msrc
            reg_of[name]    = "setfn!($(repr(name)), $ident, $arity)"
        else
            push!(toplevel, Compiler.compile_top(f))
        end
    end
end
methods       = [method_of[n] for n in order_names]
registrations = [reg_of[n]    for n in order_names]
println("  compiled $(length(order_names)) unique defuns ($nskipped prim-collision skipped), $(length(toplevel)) top-level forms")

# 3. Emit src/kernel_generated.jl. This file is `include`d INTO the Prims module,
#    so every name it references (KDATA, S, APP, PARTIAL, BIND, MKTREE, setfn!,
#    force, ShenFn, and the K_ methods themselves) resolves there.
open(joinpath(SRC, "kernel_generated.jl"), "w") do io
    println(io, "# AUTO-GENERATED by bin/gen_kernel.jl — DO NOT EDIT.")
    println(io, "# Baked ShenOSKernel 41.2: $(length(order_names)) kernel methods, compiled ahead of time")
    println(io, "# so precompilation/sysimage bake them (fast boot, no per-startup Core.eval).")
    println(io)
    for m in methods
        println(io, m)
        println(io)
    end
    println(io, "# Register the baked methods into F/ARITY and run top-level kernel forms.")
    println(io, "function _register_baked_kernel!()")
    for r in registrations
        println(io, "    ", r)
    end
    for e in toplevel
        println(io, "    ", e)
    end
    println(io, "    return nothing")
    println(io, "end")
end

# 4. Serialize the literal + arity tables the baked code depends on.
serialize(joinpath(PROJ, "kernel_kdata.jls"),  copy(Compiler.KDATA))
serialize(joinpath(PROJ, "kernel_arity.jls"),  copy(Compiler.ARITY))

println("Wrote src/kernel_generated.jl ($(filesize(joinpath(SRC, "kernel_generated.jl"))) bytes)")
println("Wrote kernel_kdata.jls ($(length(Compiler.KDATA)) entries), kernel_arity.jls ($(length(Compiler.ARITY)) entries)")
println("Next: add the include to Prims.jl (guarded), then rebuild the sysimage.")
