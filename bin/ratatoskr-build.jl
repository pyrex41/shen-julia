#!/usr/bin/env julia
# Ratatoskr stage-2 builder for shen-julia.
#
#   julia --project=<shen-julia> bin/ratatoskr-build.jl <shaken-dir> <out-dir> [--sysimage]
#
# <shaken-dir> is a Ratatoskr stage-1 output directory (kernel.kl = the tree-shaken
# 41.2 kernel defuns in load order, one or more user .kl files, ratatoskr.manifest.txt).
# This builder compiles the shaken kernel + user KL ahead of time with shen-julia's own
# compiler (the SAME technique as bin/gen_kernel.jl, but on the minimal slice) and emits
# a self-contained Julia artifact project under <out-dir>:
#
#   <out-dir>/src/{Runtime,Compiler,Prims,Boot,ShenJulia}.jl   shen-julia runtime (copied)
#   <out-dir>/src/kernel_generated.jl   the SHAKEN kernel+user defuns as baked methods,
#                                       _register_baked_kernel!() (registrations) and
#                                       _run_user!() (the user program's top-level forms)
#   <out-dir>/kernel_arity.jls          shaken arity table
#   <out-dir>/Project.toml, Manifest.toml   (copied from shen-julia)
#   <out-dir>/main.jl                   boot (baked, minimal) -> shen.initialise -> run user
#   <out-dir>/run                       launcher (uses prog.sys if built)
#
# With --sysimage it additionally bakes <out-dir>/prog.sys (PackageCompiler) so the
# artifact starts in ~1s instead of paying first-call JIT — the Julia analogue of the
# Lisp builder's saved image / the Go/Rust native binaries.

using ShenJulia
using ShenJulia: Compiler, Boot, Runtime, Prims
using Serialization

length(ARGS) >= 2 || error("usage: ratatoskr-build.jl <shaken-dir> <out-dir> [--sysimage]")
const SHAKEN = abspath(ARGS[1])
const OUT    = abspath(ARGS[2])
const DO_SYS = "--sysimage" in ARGS
const SJ_SRC = joinpath(dirname(@__DIR__), "src")
const SJ_ROOT = dirname(@__DIR__)

isdefun(f) = Runtime.is_cons(f) && Runtime.is_symbol(Compiler.car(f)) && Compiler.car(f).name == "defun"
defun_name(f) = Compiler.car(Compiler.cdr(f)).name

# ---- 1. manifest ---------------------------------------------------------
manifest = Dict{String,String}()
user_files = String[]
for ln in eachline(joinpath(SHAKEN, "ratatoskr.manifest.txt"))
    occursin('=', ln) || continue
    k, v = split(ln, '=', limit=2)
    if k == "user"
        push!(user_files, v)
    else
        manifest[k] = v
    end
end
kernel_kl = joinpath(SHAKEN, get(manifest, "kernel", "kernel.kl"))
initfn    = get(manifest, "init", "shen.initialise")
println("Building artifact from $SHAKEN")
println("  kernel=$(basename(kernel_kl)) init=$initfn user=$(user_files)")

# ---- 2. read + prescan kernel and user forms ----------------------------
kernel_forms = Runtime.read_all(read(kernel_kl, String))
user_form_lists = [(uf, Runtime.read_all(read(joinpath(SHAKEN, uf), String))) for uf in user_files]
Compiler.prescan(kernel_forms)
for (_, fs) in user_form_lists
    Compiler.prescan(fs)
end

# ---- 3. compile (defuns -> baked methods; toplevel kept) -----------------
order_names = String[]
method_of   = Dict{String,String}()
reg_of      = Dict{String,String}()
kernel_top  = String[]    # kernel top-level non-defun forms (license strings etc.)
user_top    = String[]    # user program top-level forms (the actual program)

function take_defun!(f)
    name = defun_name(f)
    if name in Compiler.PRIMS            # host prim already provides K_<name>
        Compiler.cdefun_parts(f); return
    end
    _, arity, ident, msrc = Compiler.cdefun_parts(f)
    haskey(method_of, name) || push!(order_names, name)   # last-wins on dup names
    method_of[name] = msrc
    reg_of[name]    = "setfn!($(repr(name)), $ident, $arity)"
end

for f in kernel_forms
    isdefun(f) ? take_defun!(f) : push!(kernel_top, Compiler.compile_top(f))
end
for (_, fs) in user_form_lists
    for f in fs
        isdefun(f) ? take_defun!(f) : push!(user_top, Compiler.compile_top(f))
    end
end
println("  $(length(order_names)) baked methods, $(length(user_top)) user top-level forms")

# ---- 4. emit the artifact project ---------------------------------------
mkpath(joinpath(OUT, "src"))
for fn in ("Runtime.jl", "Compiler.jl", "Prims.jl", "Boot.jl", "ShenJulia.jl")
    cp(joinpath(SJ_SRC, fn), joinpath(OUT, "src", fn); force=true)
end
cp(joinpath(SJ_ROOT, "Project.toml"), joinpath(OUT, "Project.toml"); force=true)
isfile(joinpath(SJ_ROOT, "Manifest.toml")) &&
    cp(joinpath(SJ_ROOT, "Manifest.toml"), joinpath(OUT, "Manifest.toml"); force=true)

open(joinpath(OUT, "src", "kernel_generated.jl"), "w") do io
    println(io, "# AUTO-GENERATED by ratatoskr-build.jl — shaken ShenOSKernel 41.2 + user program.")
    println(io, "# $(length(order_names)) baked methods.")
    println(io)
    for n in order_names
        println(io, method_of[n]); println(io)
    end
    println(io, "function _register_baked_kernel!()")
    for n in order_names
        println(io, "    ", reg_of[n])
    end
    for e in kernel_top
        println(io, "    ", e)
    end
    println(io, "    return nothing")
    println(io, "end")
    println(io)
    println(io, "# The user program's top-level forms — run after shen.initialise.")
    println(io, "function _run_user!()")
    for e in user_top
        println(io, "    ", e)
    end
    println(io, "    return nothing")
    println(io, "end")
end
serialize(joinpath(OUT, "kernel_arity.jls"), copy(Compiler.ARITY))

# main.jl: boot the baked shaken kernel, then run the user program forms.
open(joinpath(OUT, "main.jl"), "w") do io
    write(io, """
    # Ratatoskr shen-julia artifact entry point.
    using ShenJulia
    using ShenJulia: Prims
    boot!(get(ENV, "SHEN_VERBOSE", "") == "1")   # baked shaken kernel + shen.initialise
    Prims.with_shen_stack() do
        getfield(Prims, :_run_user!)()
    end
    flush(stdout)
    """)
end

# run wrapper
open(joinpath(OUT, "run"), "w") do io
    write(io, """#!/bin/sh
    HERE="\$(cd "\$(dirname "\$0")" && pwd)"
    SYS=""
    [ -f "\$HERE/prog.sys" ] && SYS="--sysimage=\$HERE/prog.sys"
    exec julia --project="\$HERE" \$SYS "\$HERE/main.jl" "\$@"
    """)
end
chmod(joinpath(OUT, "run"), 0o755)
println("  wrote artifact project to $OUT")

# ---- 5. optional per-program sysimage ------------------------------------
if DO_SYS
    using PackageCompiler
    precomp = joinpath(OUT, "_precompile.jl")
    open(precomp, "w") do io
        write(io, """
        using ShenJulia
        using ShenJulia: Prims
        boot!(false)
        try; Prims.with_shen_stack() do; getfield(Prims, :_run_user!)(); end; catch e; end
        """)
    end
    println("  building per-program sysimage prog.sys (PackageCompiler; minutes)...")
    create_sysimage(["ShenJulia"];
        sysimage_path = joinpath(OUT, "prog.sys"),
        precompile_execution_file = precomp,
        project = OUT,
    )
    rm(precomp; force=true)
    println("  prog.sys: ", round(filesize(joinpath(OUT, "prog.sys")) / 1024 / 1024, digits=1), " MiB")
end

println("Done. Run it with: $OUT/run")
