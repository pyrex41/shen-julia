#!/usr/bin/env julia
# Ahead-of-time StLib generator.
#
# Tarver's S41.2 refresh ships the standard library as Shen SOURCE under
# Lib/StLib (loaded at install time by install.shen, which type-checks it). A
# from-source load costs ~50s, so — exactly as the kernel is baked by
# bin/gen_kernel.jl — we compile StLib ahead of time and bake the resulting
# methods into the package image.
#
# Mechanism: boot the (already baked) kernel, record every KL `(defun …)` form
# eval_kl compiles while we `(load "install.shen")`, then re-emit those forms
# through the SAME cdefun_parts path gen_kernel uses. Writes:
#
#   src/stlib_generated.jl    top-level `function K_…` methods + a
#                             `_register_baked_stlib!()` that setfn!-registers them
#                             at boot (after the kernel's own registration).
#   kernel_stlib_arity.jls    the full post-StLib arity table (superset of
#                             kernel_arity.jls) the post-boot codegen needs.
#
# Run: julia --project=. bin/gen_stlib.jl   (then rebuild the sysimage)
#
# Requires the StLib sources; set SHEN_STLIB_DIR or vendor them under lib/stlib.

using ShenJulia
using ShenJulia: Compiler, Boot, Runtime, Prims
using Serialization

const PROJ = dirname(@__DIR__)
const SRC  = joinpath(PROJ, "src")

stlibdir = get(ENV, "SHEN_STLIB_DIR", joinpath(PROJ, "lib", "stlib"))
isfile(joinpath(stlibdir, "install.shen")) || error("StLib not found at $stlibdir (set SHEN_STLIB_DIR)")
println("Generating baked StLib from: ", stlibdir)

# 1. Boot the baked kernel (kernel-only — never stack a source StLib load on top of
#    an already-baked one), then load StLib recording every defun form it compiles.
ENV["SHEN_SKIP_BAKED_STLIB"] = "1"
boot!(false)
Prims.define_named!("y-or-n?", 1, _ -> true)   # install.shen asks y-or-n? in some libs
# Load hushed: shen-julia carries a custom pr/*hush* override (from the kernel
# migration), and the fleet found that routing install.shen's chatter through a
# custom pr path can corrupt subsequent evaluation. Hushing also keeps the gen log
# clean. (We record the defun *forms*, independent of printed output.)
Prims.GLOBALS["*hush*"] = true
forms = Any[]
Prims.DEFUN_RECORDER[] = forms
Prims.with_shen_stack() do
    cd(stlibdir) do
        Prims.force(Base.invokelatest(Prims.F["load"], "install.shen"))
    end
end
Prims.DEFUN_RECORDER[] = nothing
println("  captured $(length(forms)) defun forms; arity table now $(length(Compiler.ARITY))")

# 2. Re-emit through cdefun_parts (last-wins per name, like gen_kernel — a few names
#    are defined more than once, and two baked top-level `function K_x` would be a
#    forbidden precompile duplicate). Skip any name backed by a host primitive.
isdefun(f) = Runtime.is_cons(f) && Runtime.is_symbol(Compiler.car(f)) && Compiler.car(f).name == "defun"
order_names = String[]
method_of   = Dict{String,String}()
reg_of      = Dict{String,String}()
arity_of    = Dict{String,Int}()
nskipped = 0
# Datatype type-recognisers (name#type) generated by StLib's own datatypes
# (prettyprint's `print`, the maths datatypes). These are type-check-time artifacts —
# upstream's install.shen ends with (preclude-all-but []), i.e. StLib's datatypes are
# NOT meant to stay active — and baking them shadows a user/test datatype of the same
# name (e.g. c-minus's `print`), whose runtime redefinition a baked recogniser would
# hide (world-age). Runtime StLib functions never call them, so drop them.
is_recogniser(nm) = endswith(nm, "#type")
excluded = String[]
for f in forms
    isdefun(f) || continue
    name = Compiler.car(Compiler.cdr(f)).name
    if name in Compiler.PRIMS
        Compiler.cdefun_parts(f); global nskipped += 1; continue
    end
    if is_recogniser(name)
        Compiler.cdefun_parts(f); push!(excluded, name); continue
    end
    _, arity, ident, msrc = Compiler.cdefun_parts(f)
    haskey(method_of, name) || push!(order_names, name)
    method_of[name] = msrc
    reg_of[name]    = "setfn!($(repr(name)), $ident, $arity)"
    arity_of[name]  = arity
end
methods       = [method_of[n] for n in order_names]
registrations = [reg_of[n]    for n in order_names]
# Also register the stdlib arities in the kernel's *property-vector* (via the kernel's
# own shen.initialise-arity-table), NOT just the host F/ARITY tables. Without this the
# kernel's function-reference machinery — (fn filter), passing a stdlib fn as a value,
# higher-order use — reports "fn: … is undefined" because (arity name) returns -1.
# A from-source install does this as a side effect of loading each defun; the baked
# path must do it explicitly.
arity_list = join(["S($(repr(n))), $(arity_of[n])" for n in order_names], ", ")
arity_reg  = "$(Compiler.fnident("shen.initialise-arity-table"))(MKLIST([$arity_list], NIL))"
println("  emitting $(length(order_names)) unique stdlib methods ($nskipped prim-collision skipped)")

# 3. Emit src/stlib_generated.jl (included INTO the Prims module, same as the kernel).
open(joinpath(SRC, "stlib_generated.jl"), "w") do io
    println(io, "# AUTO-GENERATED by bin/gen_stlib.jl — DO NOT EDIT.")
    println(io, "# Baked Shen StLib (Tarver S41.2 2026-07-11 refresh, Lib/StLib): $(length(order_names)) methods.")
    println(io)
    for m in methods
        println(io, m); println(io)
    end
    println(io, "function _register_baked_stlib!()")
    for r in registrations
        println(io, "    ", r)
    end
    println(io, "    ", arity_reg)
    println(io, "    return nothing")
    println(io, "end")
end

# 4. Serialize the post-StLib arity table (superset of kernel_arity.jls), minus the
#    excluded recognisers — otherwise the merged ARITY would make the compiler emit a
#    direct call to a K_…#type method that was never baked.
arity_out = copy(Compiler.ARITY)
for n in excluded; delete!(arity_out, n); end
serialize(joinpath(PROJ, "kernel_stlib_arity.jls"), arity_out)
println("Wrote src/stlib_generated.jl ($(filesize(joinpath(SRC, "stlib_generated.jl"))) bytes)")
println("Wrote kernel_stlib_arity.jls ($(length(Compiler.ARITY)) entries)")
println("Next: guard the include in Prims.jl, wire _register_baked_stlib! into Boot, bump the guard, rebuild sysimage.")
